    private void tampilBarang() {
        List<barang> lsBrg = new ArrayList<barang>();
        Executebarang exeBrg = new Executebarang();
        lsBrg = exeBrg.getAllbarang();
        String[][] dtBrg = new String[lsBrg.size()][5];
        int i = 0;
        for (barang brg : lsBrg) {
            dtBrg[i][0] = brg.getKodebarang();
            dtBrg[i][1] = brg.getTipebarang();
            dtBrg[i][2] = brg.getNamaBarang();
            dtBrg[i][3] = String.valueOf(brg.getHarga()); // Ubah ke String
            dtBrg[i][4] = String.valueOf(brg.getKuantitas()); // Ubah ke String
            i++;
        }
        tbl_pemesanan.setModel(new javax.swing.table.DefaultTableModel(
                dtBrg,
                new String[]{
                    "Kode Barang", "Tipe Barang", "Nama Barang", "Harga Barang", "Qty"
                }
        ));
    }
    
