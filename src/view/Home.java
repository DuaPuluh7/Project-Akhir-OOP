    private void refreshTable() {
    ConnectionManager conMan = new ConnectionManager();
    Connection conn = conMan.logOn();
    try {
        // Ambil data kuantitas barang terbaru dari database
        String query = "SELECT kodebarang, kuantitas FROM barang";
        Statement stmt = conn.createStatement();
        ResultSet rs = stmt.executeQuery(query);

        // Perbarui kuantitas barang dalam model tabel
        DefaultTableModel model = (DefaultTableModel) tbl_pemesanan.getModel();

        while (rs.next()) {
            String kodeBarang = rs.getString("kodebarang");
            int kuantitasBarang = rs.getInt("kuantitas");

            // Perbarui kuantitas barang dalam model tabel
            for (int i = 0; i < model.getRowCount(); i++) {
                String kodeBarangTabel = (String) model.getValueAt(i, 0);
                if (kodeBarang.equals(kodeBarangTabel)) {
                    model.setValueAt(kuantitasBarang, i, 4);
                    break;
                }
            }
        }

        tbl_pemesanan.repaint(); // Refresh tampilan tabel
    } catch (SQLException ex) {
        Logger.getLogger(ConnectionManager.class.getName()).log(Level.SEVERE, null, ex);
    } finally {
        conMan.logOff();
    }
}
