    private void t_BeliMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_t_BeliMouseClicked
     if (!isPengecheckanSelesai) {
            JOptionPane.showMessageDialog(null, "Lakukan proses pengecheckan terlebih dahulu.");
            return; // Menghentikan proses pembelian jika pengecheckan belum selesai
        }

        int baris = tbl_pemesanan.getSelectedRow();
        if (baris == -1) {
            JOptionPane.showMessageDialog(null, "Pilih dahulu barang yang akan dibeli.");
            return;
        }

        // Mendapatkan data barang yang akan dibeli
        Executetransaksi tarsk = new Executetransaksi();
        String kodeBarang = (String) tbl_pemesanan.getValueAt(baris, 0);
        int jumlahBeli = Integer.parseInt(t_jumlah.getText());
        String tipebarang = (String) tbl_pemesanan.getValueAt(baris, 1);
        String namaBarang = (String) tbl_pemesanan.getValueAt(baris, 2);

        // Mendapatkan kuantitas barang dari database
        int kuantitasBarang = tarsk.getKuantitasBarangFromDatabase(kodeBarang);

        // Mengecek apakah kuantitas cukup untuk pembelian
        if (jumlahBeli > kuantitasBarang) {
            JOptionPane.showMessageDialog(null, "Pembelian gagal karena kuantitas tidak mencukupi.");
            return;
        }

        // Menghitung total harga
        int harga = Integer.parseInt((String) tbl_pemesanan.getValueAt(baris, 3));
        Transaksi trsk = new Transaksi();
        int totalHarga = trsk.hitungTotalHarga(harga, jumlahBeli);

//         Menerapkan diskon berdasarkan kode voucher jika kode voucher tidak kosong
        String kodeVoucher = t_kodeVoucher.getText();
        if (!kodeVoucher.isEmpty()) {
            totalHarga = trsk.hitungTotalHarga(kodeVoucher, totalHarga);
        }

        // Menampilkan konfirmasi penggunaan voucher jika kode voucher tidak kosong
        if (!kodeVoucher.isEmpty()) {
            int response = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin menggunakan voucher?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (response == JOptionPane.NO_OPTION) {
                totalHarga = trsk.hitungTotalHarga(harga, jumlahBeli); // Kembali ke total harga sebelum diskon
            }
        }

        boolean pembelianBerhasil = tarsk.lakukanPembelian(kodeBarang, jumlahBeli, totalHarga, namaBarang, tipebarang, harga, dbEmail, t_Size);
        if (pembelianBerhasil) {
            tarsk.kurangiKuantitasBarang(kodeBarang, jumlahBeli); // Mengurangi kuantitas barang di database

            JOptionPane.showMessageDialog(null, "Pembelian berhasil."); // Menampilkan pesan pembelian berhasil

            kosongkanDataBarang(); // Mengosongkan data di panel barang
            refreshTable(); // Memperbarui tampilan tabel

            tampilTransaksi(); // Menampilkan riwayat transaksi terbaru
        } else {
            JOptionPane.showMessageDialog(null, "Pembelian gagal.");
        }

    }//GEN-LAST:event_t_BeliMouseClicked
