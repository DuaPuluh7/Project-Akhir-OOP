    private void refreshTableBarang() {
        Executebarang executebarang = new Executebarang();
        List<barang> daftarBarang = executebarang.getAllbarang();

        DefaultTableModel model = (DefaultTableModel) t_Barang.getModel();
        model.setRowCount(0); // Hapus semua baris yang ada dalam model tabel

        for (barang barang : daftarBarang) {
            String kodeBarang = barang.getKodebarang();
            String namaBarang = barang.getNamaBarang();
            String tipeBarang = barang.getTipebarang();
            String size = barang.getSize();
            int hargaBarang = barang.getHarga();
            int kuantitas = barang.getKuantitas();

            model.addRow(new Object[]{kodeBarang, namaBarang, tipeBarang, size, hargaBarang, kuantitas});
        }

        t_Barang.repaint(); // Refresh tampilan tabel
    }
