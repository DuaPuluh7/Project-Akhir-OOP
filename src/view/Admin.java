    private void refreshTableUsr() {
        ConnectionManager conMan = new ConnectionManager();
        Connection conn = conMan.logOn();
        try {
            // Ambil data pengguna terbaru dari database
            String query = "SELECT nama, email, alamat, notelepon,umur,password FROM user";
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(query);

            // Perbarui model tabel
            DefaultTableModel model = (DefaultTableModel) t_user.getModel();
            model.setRowCount(0); // Hapus semua baris yang ada dalam model tabel

            while (rs.next()) {
                String nama = rs.getString("nama");
                String email = rs.getString("email");
                String alamat = rs.getString("alamat");
                String noTelepon = rs.getString("notelepon");
                int umur = rs.getInt("umur");
                String password = rs.getString("password");

                // Tambahkan baris baru ke model tabel
                model.addRow(new Object[]{email, nama , umur, noTelepon, alamat,password});
            }

            t_user.repaint(); // Refresh tampilan tabel
        } catch (SQLException ex) {
            Logger.getLogger(ConnectionManager.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            conMan.logOff();
        }
    }
