    private void jToggleButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jToggleButton1MouseClicked
        String nama = txtNama.getText();
        String email = txtGmail.getText();
        String alamat = txtAlamat.getText();
        String noTelepon = txtTelepon.getText();
        int umur;
        try {
            umur = Integer.parseInt(txtUmur.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Umur harus diisi dengan angka.");
            return; // Menghentikan proses registrasi
        }
        String password = String.valueOf(txtPassword.getPassword());
        String confirmPassword = String.valueOf(txtConfirmPassword.getPassword());

        // Validasi kolom kosong
        if (nama.isEmpty() || email.isEmpty() || alamat.isEmpty() || noTelepon.isEmpty() || txtUmur.getText().isEmpty() || password.isEmpty() || confirmPassword.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Harap isi semua kolom.");
            return; // Menghentikan proses registrasi
        }

        // Validasi apakah password sama dengan confirm password
        if (!password.equals(confirmPassword)) {
            JOptionPane.showMessageDialog(null, "Password tidak cocok dengan Confirm Password. Perbaiki password.");
            return; // Menghentikan proses registrasi
        }

        // Proses registrasi user
//        user user = new user(nama, email, alamat, noTelepon, umur, password);
        user usr  = new user(nama,email,alamat,noTelepon,umur,password);
        Executeuser exeUsr = new Executeuser();
        int success = exeUsr.insertData(usr);

        if (success > 0) {
            JOptionPane.showMessageDialog(null, "Registrasi berhasil");
            txtNama.setText("");
            txtGmail.setText("");
            txtAlamat.setText("");
            txtTelepon.setText("");
            txtUmur.setText("");
            txtPassword.setText("");
            txtConfirmPassword.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Registrasi gagal. Silakan coba lagi.");
        }

    }//GEN-LAST:event_jToggleButton1MouseClicked

